// Definindo algumas variáveis numéricas
let x = 2;
let y = 3;

// Adição
console.log("Adição (x + y):", x + y);  // Resultado: 5

// Subtração
console.log("Subtração (x - y):", x - y);  // Resultado: -1

// Multiplicação
console.log("Multiplicação (x * y):", x * y);  // Resultado: 6

// Divisão
console.log("Divisão (x / y):", x / y);  // Resultado: Aproximadamente 0.6667

// Divisão Inteira
console.log("Divisão Inteira (Math.floor(x / y)):", Math.floor(x / y));  // Resultado: 0 (arredondado para baixo)

// Módulo
console.log("Módulo (x % y):", x % y);  // Resultado: 2 (resto da divisão de 2 por 3)

// Exponenciação
console.log("Exponenciação (Math.pow(x, y)):", Math.pow(x, y));  // Resultado: 8 (2 elevado a 3)


// Number: Inteiros e de ponto flutuante
let numero_inteiro = 42;
let numero_float = 3.14;

// String: String
let texto = "Olá, mundo!";

// Boolean: Verdadeiro ou Falso
let verdadeiro = true;
let falso = false;

// Null: Representa a ausência de valor
let sem_valor = null;

// Object: Tipo para objetos (coleções não primitivas)
let meu_objeto = { chave: "valor", numero: 123 };
let array = [1, 2, 3];

// Exibindo todos os tipos de dados
console.log("Número inteiro:", numero_inteiro);
console.log("Número de ponto flutuante:", numero_float);
console.log("String:", texto);
console.log("Booleano (verdadeiro):", verdadeiro);
console.log("Booleano (falso):", falso);
console.log("Null:", sem_valor);
console.log("Object:", meu_objeto);
console.log("Array:", array);


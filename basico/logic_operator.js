// Definindo algumas variáveis booleanas
let a = true;
let b = false;
let c = true;

// Usando o operador lógico '&&' (and)
console.log("a && b:", a && b);  // Deve ser false, pois b é false
console.log("a && c:", a && c);  // Deve ser true, pois ambos a e c são true

// Usando o operador lógico '||' (or)
console.log("a || b:", a || b);  // Deve ser true, pois a é true
console.log("b || c:", b || c);  // Deve ser true, pois c é true

// Usando o operador lógico '!' (not)
console.log("!a:", !a);  // Deve ser false, pois a é true
console.log("!b:", !b);  // Deve ser true, pois b é false

